#include <stdio.h>

int main(void) {

    int a = 10;
    int b = 20;

    int one = a + b;
    int two = a - b;
    int three = a * b;
    int four = a / b;

    printf("a + b = %d\n", one);
    printf("a - b = %d\n", two);
    printf("a * b = %d\n", three);
    printf("a / b = %d\n", four);

    return 0;


}